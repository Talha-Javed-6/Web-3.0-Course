//A program that calculates the area of the rectangle

//Variable for the length of the Rectangle
var lengthOfRectangle:number=7;

//Variable for the width of the Rectangle
var widthOfRectangle:number=4;

//Variable for the Area of the Rectangle
//Area of Rectangle = Length * Width
var areaOfRectangle:number= lengthOfRectangle * widthOfRectangle;
//Displaying the result 
console.log("The Area of the Rectangle with length",lengthOfRectangle,"units and width",widthOfRectangle,"units =",areaOfRectangle,"Units");